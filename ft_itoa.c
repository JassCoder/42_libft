#include "libft.h"

static int	count_digits(int n)
{
	int	count;

	count = 0;
	if (n <= 0)
		count++;
	while (n != 0)
	{
		n /= 10;
		count++;
	}
	return (count);
}

static char	*fill_nbr(char *str, int n, int len)
{
	if ( n == -2147483648)
	{
		ft_strlcpy(str,"-2147483648",12);
		return (str);
	}
	if (n < 0)
	{
		str[0] = '-';
		n = -n;
	}
	str[len] = '\0';
	while (n > 0)
	{
		str[--len] = (n % 10) + '0';
		n /= 10;
	}
	return (str);
}

char	*ft_itoa(int n)
{
	char	*str;
	int		len;

	if (n == 0)
		return (ft_strdup("0"));
	len = (count_digits(n));
	str = malloc(len + 1);
	if (!str)
		return (NULL);
	return (fill_nbr(str, n, len));
}